package homework.ROBIN.e01;

/**
 * Matemaattisia apumetodeja sisältävä luokka.
 */
public class MyMath {
    /**
     * Tarkistaa onko annettu luku positiivinen.
     * @param number Tarkistettava luku.
     * @return Jos luku on posi, palauttaa True, muuten False.
     */
    public static boolean isPositive(int number) {
        return number > 0;
    }
}
